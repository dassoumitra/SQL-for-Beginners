# SQL Subquery Interview Questions

### 1. What is a subquery in SQL?

A subquery is a query nested inside another SQL query.

---

### 2. What are the types of subqueries?

* Single-row subquery
* Multi-row subquery
* Correlated subquery

---

### 3. Where can subqueries be used?

* SELECT
* WHERE
* FROM
* HAVING

---

### 4. What is a correlated subquery?

A correlated subquery depends on the outer query and executes once for each row.

---

### 5. Difference between JOIN and Subquery?

JOIN combines tables horizontally, while a subquery retrieves results used by the outer query.

---

### 6. What does EXISTS do?

EXISTS checks if the subquery returns any rows.

---

### 7. Difference between IN and EXISTS?

IN compares values from a list, while EXISTS checks if rows exist in the subquery.
